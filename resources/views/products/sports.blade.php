     <script>
    const featureItems = document.querySelectorAll('#sportsFeaturesList li');
    const image = document.getElementById('sportsSingleImage');
    const imageSection = document.getElementById('sportsImageColumn');

    window.addEventListener('DOMContentLoaded', () => {
      if (featureItems.length > 0) {
        const first = featureItems[0];
        image.src = first.getAttribute('data-img');
        first.classList.add('activeFeature');
      }
    });

    featureItems.forEach(item => {
      item.addEventListener('click', () => {
        const newImg = item.getAttribute('data-img');
        image.src = newImg;
        updateActive(item);

        if (window.innerWidth <= 768) {
          imageSection.scrollIntoView({ behavior: 'smooth' });
        }
      });
    });

    function updateActive(selectedItem) {
      featureItems.forEach(el => el.classList.remove('activeFeature'));
      selectedItem.classList.add('activeFeature');
    }
  </script>
