  <script>
  document.addEventListener('DOMContentLoaded', () => {
    const items = document.querySelectorAll('#app-list li');
    const img = document.getElementById('app-image');
    const basePath = "{{ asset('images/applications') }}";

    if (!img) return;

    let currentImage = img.src;
    let lastRequestedImage = null;

    items.forEach(item => {
      item.addEventListener('mouseenter', () => {
        const imageFilename = item.dataset.image;
        const newImage = `${basePath}/${imageFilename}`;

        if (newImage === currentImage) return;

        // Save what we're trying to show now
        lastRequestedImage = newImage;

        // Create a temp image to preload
        const preloadImg = new Image();
        preloadImg.src = newImage;

        preloadImg.onload = () => {
          // Only proceed if this is still the most recent request
          if (lastRequestedImage !== newImage) return;

          img.style.transition = 'opacity 0.3s ease';
          img.style.opacity = 0;

          setTimeout(() => {
            img.src = newImage;
            img.onload = () => {
              img.style.opacity = 1;
              currentImage = newImage;
            };
          }, 200);
        };
      });
    });
  });



  
